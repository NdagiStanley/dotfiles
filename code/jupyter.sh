# Setting Up Jupyter Notebook with JavaScript, Python2 and Python3 Support

# Based on: https://www.sean-lan.com/2016/09/12/jupyter-notebook-set-up/

# Steps:
brew install python python@2
pip2 install ipython notebook
pip3 install ipython notebook
ipython2 kernel install
ipython3 kernel install
npm install -g ijavascript
ijs

# Then you'll be running `jupyter notebook` (at the root directory of choice)
